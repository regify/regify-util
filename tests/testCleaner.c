void cleanerCb (void* user_data, const char *key, const char* subst) {
    ruString rs = (ruString)user_data;
    int db = 0;
    if (subst) {
        ruStringAppendf(rs, "%s:%s|", key, subst);
        if(db) ruVerbLogf("%s:%s", key, subst);
    } else {
        ruStringAppendf(rs, "%s|", key);
        if(db) ruVerbLogf("%s", key);
    }
}

START_TEST ( work ) {
    int32_t ret, exp;
    const char *test = "";
    const char *retText = "%s failed wanted ret '%d' but got '%d'";
    struct ioCtx rc, wc;
    memset(&rc, 0, sizeof(struct ioCtx));
    memset(&wc, 0, sizeof(struct ioCtx));
    wc.len = 1024;
    wc.buf = ruMalloc0(wc.len, char);
    wc.cur = wc.buf;

    exp = RUE_OK;
    ruCleaner c = ruCleanNew(11);
    fail_unless(c != NULL, "cleaner object was null");

    test = "ruCleanAdd";
    ret = ruCleanAdd(c, "zap", "this");
    fail_unless(ret == exp, retText, test, exp, ret);

    ret = ruCleanAdd(c, "foo", "^");
    fail_unless(ret == exp, retText, test, exp, ret);

    ret = ruCleanAdd(c, "foo2", "bar1");
    fail_unless(ret == exp, retText, test, exp, ret);

    ruString rs = NULL;

    test = "ruCleanDump";
    rs = ruStringNew("");
    ret = ruCleanDump(c, cleanerCb, rs);
    fail_unless(ret == exp, retText, test, exp, ret);
    ck_assert_str_eq(ruStringGetCString(rs), "foo2:bar1|foo:^|fo|f|zap:this|za|z||");
    ruStringFree(rs, false);


    // test duplicate entries bug 3616
    test = "ruCleanAdd";
    ret = ruCleanAdd(c, "foo2", "bar2");
    fail_unless(ret == exp, retText, test, exp, ret);

    test = "ruCleanDump";
    rs = ruStringNew("");
    ret = ruCleanDump(c, cleanerCb, rs);
    fail_unless(ret == exp, retText, test, exp, ret);
    ck_assert_str_eq(ruStringGetCString(rs), "foo2:bar2|foo:^|fo|f|zap:this|za|z||");
    ruStringFree(rs, false);

    test = "ruCleanAdd";
    ret = ruCleanAdd(c, "2foo", "2bar");
    fail_unless(ret == exp, retText, test, exp, ret);

    test = "ruCleanIo";
    ret = ruCleanIo(c, &myread, &rc, &mywrite, &wc);
    fail_unless(ret == exp, retText, test, exp, ret);

    rc.buf = "foo2 and foo2foo had foo for bar2 in foo";
    rc.cur = rc.buf;
    rc.len = strlen(rc.buf);

    test = "ruCleanNow";
    const char* exStr = "bar2 and bar2^ had ^ for bar2 in ^";
    ret = ruCleanNow(c);
    fail_unless(ret == exp, retText, test, exp, ret);
    ck_assert_str_eq(wc.buf, exStr);

    ruFree(wc.buf);
    test = "ruCleanRemove";
    ruCleanRemove(c, "2foo");
    fail_unless(ret == exp, retText, test, exp, ret);

    test = "ruCleanDump";
    rs = ruStringNew("");
    ret = ruCleanDump(c, cleanerCb, rs);
    fail_unless(ret == exp, retText, test, exp, ret);
    ck_assert_str_eq(ruStringGetCString(rs), "foo2:bar2|foo:^|fo|f|zap:this|za|z||");
    ruStringFree(rs, false);

    test = "ruCleanRemove";
    ruCleanRemove(c, "2foo");
    fail_unless(ret == exp, retText, test, exp, ret);

    ruCleanRemove(c, "fo");
    fail_unless(ret == exp, retText, test, exp, ret);

    ruCleanRemove(c, "foo");
    fail_unless(ret == exp, retText, test, exp, ret);

    ruCleanRemove(c, "fooooobar");
    fail_unless(ret == exp, retText, test, exp, ret);

    test = "ruCleanDump";
    rs = ruStringNew("");
    ret = ruCleanDump(c, cleanerCb, rs);
    fail_unless(ret == exp, retText, test, exp, ret);
    ck_assert_str_eq(ruStringGetCString(rs), "foo2:bar2|foo|fo|f|zap:this|za|z||");
    ruStringFree(rs, false);

    ruCleanFree(c);
}
